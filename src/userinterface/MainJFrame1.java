    private void changePanel(UserAccount userAccount) {
        
        if (userAccount != null) {
            String greetings = "Admin";

            if (userAccount instanceof Hospital) {
                greetings = ((Hospital) userAccount).getHospitalName();
            } else if (userAccount instanceof User) {
                greetings = ((User) userAccount).getName();
            } else if (userAccount instanceof Shop) {
                greetings = ((Shop) userAccount).getShopName();
            } else if (userAccount instanceof DeliveryMan) {
                greetings = ((DeliveryMan) userAccount).getName();
            } else if (userAccount instanceof Staff) {
                greetings = ((Staff) userAccount).getName();
            } else if (userAccount instanceof Hospital) {
                greetings = ((Hospital) userAccount).getHospitalName();
            } else if (userAccount instanceof FcStaff) {
                greetings = ((FcStaff) userAccount).getName();
            } else if (userAccount instanceof FitnessCenter) {
                greetings = ((FitnessCenter) userAccount).getFitnessCenterName();
            } else if (userAccount instanceof AmbulanceService) {
                greetings = ((AmbulanceService) userAccount).getName();
            }
//            else if (userAccount instanceof Restaurant) {
//                greetings = ((Restaurant) userAccount).getName();
//            } else if (userAccount instanceof DeliveryMan) {
//                greetings = ((DeliveryMan) userAccount).getName();
//            }

            lblGreeting.setText(greetings);

            btnLogout.setVisible(true);
            btnLogout.setEnabled(true);

            container.setVisible(true);
            loginPanel.setVisible(false);
            signupPanel.setVisible(false);
            navbarPanel.setVisible(true);
            CardLayout crdLyt = (CardLayout) container.getLayout();
            container.add("workArea", userAccount.getRole().createWorkArea(container, userAccount, system));
            crdLyt.next(container);
        }
    }

    private void toggleForm(String type) {

        txtSUserName.setText("");
        txtSPassword.setText("");
        txtSAddress.setText("");
        txtSName.setText("");
        txtContact.setText(null);
        txtSEmail.setText("");
        txtZipcode.setText(null);

        txtPassword.setText("");
        userNameJTextField.setText("");

        switch (type) {
            case "LOGIN":
                loginPanel.setVisible(true);
                signupPanel.setVisible(false);
                break;

            case "SIGNUP":
                loginPanel.setVisible(false);
                signupPanel.setVisible(true);
                break;
        }
    }
