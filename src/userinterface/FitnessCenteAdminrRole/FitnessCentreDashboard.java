    private void setData() {

        FitnessCenterDepartmentDirectory fcd = fitnessCenter.getFcdepartmentDirectory();

        if (fcd != null) {
            int departments = fcd.getDepartmentList().size();
            lblDept.setText(String.valueOf(departments));

            int staffCount = 0, remote = 0, inperson = 0, totalSessions = 0;
            // lblTotalSessions
            int count = 0;
            int dCount = 0;

            for (FitnessCenterDepartment fc : fcd.getDepartmentList()) {

                switch (dCount) {
                    case 0:
                        tblRowDeptName1.setText(fc.getDepartmentName());
                        tblRowDeptType1.setText(fc.getType());
                        dCount++;
                        break;

                    case 1:
                        tblRowOrderName2.setText(fc.getDepartmentName());
                        tblRowDeptType2.setText(fc.getType());
                        dCount++;
                        break;

                    case 2:
                        tblRowDeptName3.setText(fc.getDepartmentName());
                        tblRowDeptType3.setText(fc.getType());
                        dCount++;
                        break;
                }

                staffCount += fc.getStaffDirectory().getStaffList().size();
                for (FcStaff fcs : fc.getStaffDirectory().getStaffList()) {
                    switch (count) {
                        case 0:
                            tblRowStaffName1.setText(fcs.getName());
                            tblRowStaffDesig1.setText(fcs.getDesignation());
                            count++;
                            break;

                        case 1:
                            tblRowStaffName2.setText(fcs.getName());
                            tblRowStaffDesig2.setText(fcs.getDesignation());
                            count++;
                            break;

                        case 2:
                            tblRowStaffName3.setText(fcs.getName());
                            tblRowStaffDesig3.setText(fcs.getDesignation());
                            count++;
                            break;
                    }

                    for (Sessions s : fcs.getSdir().getSession()) {
                        totalSessions++;
                        String sessionType = s.getIsRemote().toUpperCase();
                        if (sessionType.equalsIgnoreCase("IN-PERSON")) {
                            remote++;
                        } else if (sessionType.equals("REMOTE")) {
                            inperson++;
                        } else {
                            remote++;
                            inperson++;
                        }
                    }
                }
            }

            lblStaff.setText(String.valueOf(staffCount));
            lblTotalSessions.setText(String.valueOf(totalSessions));
            lblPersonSessions.setText(String.valueOf(inperson));
            lblRemoteSessions.setText(String.valueOf(remote));
        }
    }
