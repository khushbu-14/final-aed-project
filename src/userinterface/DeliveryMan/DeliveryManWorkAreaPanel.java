    private void manageProfile() {
        changeBtnBgs("profile");

        DeliveryMan dlvm = (DeliveryMan) userAccount;
        DeliveryManProfile deliveryManProfile = new DeliveryManProfile(ecosystem, dlvm);

        // ManageProductPanel prodcutPanel = new ManageProductPanel(mainPanel, ecosystem, userAccount);
        mainPanel.add("deliveryManProfile", deliveryManProfile);
        CardLayout layout = (CardLayout) mainPanel.getLayout();
        layout.next(mainPanel);
    }

    private void manageOrderHistory() {
        changeBtnBgs("order-history");
        ManageDeliveryManOrderHistory manageDeliveryManOrderHistory = new ManageDeliveryManOrderHistory(mainPanel, ecosystem, userAccount);

        mainPanel.add("manageDeliveryManOrderHistory", manageDeliveryManOrderHistory);
        CardLayout layout = (CardLayout) mainPanel.getLayout();
        layout.next(mainPanel);
    }

    private void changeBtnBgs(String type) {
        Color activeColor = Color.WHITE;
        Color notActiveColor = Color.getColor("03506F");

        Color activeTxtColor = Color.BLACK;
        Color nonActiveTxtColor = Color.WHITE;

        manageProfilePanel.setBackground(notActiveColor);
        manageOrdersPanel.setBackground(notActiveColor);

        manageProfile.setBackground(nonActiveTxtColor);
        manageOrders.setForeground(nonActiveTxtColor);

        if ("profile".equalsIgnoreCase(type)) {
            manageProfilePanel.setBackground(activeColor);
            manageProfile.setForeground(activeTxtColor);
        } else if ("order-history".equalsIgnoreCase(type)) {
            manageOrdersPanel.setBackground(activeColor);
            manageOrders.setForeground(activeTxtColor);
        }
    }
