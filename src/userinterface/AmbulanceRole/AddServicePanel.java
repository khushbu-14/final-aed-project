    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        AmbulanceService amb = ecosystem.getAmbulanceDirectory().getAmbulanceServiceByUserName(userAccount.getUsername());

        String userName = txtName.getText(),
                phoneNo = txtContact.getText(),
                oxygen = jcomboOxygen.getSelectedItem().toString(),
                type = jcomboType.getSelectedItem().toString();

        if (!util.isStringInputValid(userName)) {
            util.showErrorToast("Plesae enter valid Service name");
            //  JOptionPane.showMessageDialog(null, "Plesae enter valid user name");
            // JOptionPane.showMessageDialog(this, "Plesae enter valid user name", "Error", JOptionPane.ERROR_MESSAGE);
        } else if (!util.isStringInputValid(phoneNo)) {
            util.showErrorToast("Plesae enter valid contact");
            //  JOptionPane.showMessageDialog(null, "Plesae enter valid password");
            //  JOptionPane.showMessageDialog(this, "Plesae enter valid password", "Error", JOptionPane.ERROR_MESSAGE);
        } else if (!util.isStringInputValid(oxygen)) {
            util.showErrorToast("Plesae Select if oxygen available or not");
            //  JOptionPane.showMessageDialog(this, "Please enter valid name", "Error", JOptionPane.ERROR_MESSAGE);
        } else if (!util.isStringInputValid(type)) {
            util.showErrorToast("Plesae Select valid Ambulance Type");
            // JOptionPane.showMessageDialog(this, "Please enter valid address", "Error", JOptionPane.ERROR_MESSAGE);
        } else if (txtContact.getText().length() != 10) {
            util.showErrorToast("Contact must be 10 digits only");
            // JOptionPane.showMessageDialog(this, "Please enter valid address", "Error", JOptionPane.ERROR_MESSAGE);
        } else {
//            userName = userName.toLowerCase();
            String msg = userName + " created successfully!";
            if (isUpdated) {
                service.setName(userName);
                service.setContact(phoneNo);
                service.setAmbulanceType(type);
                service.setOxygenAvailability(oxygen);
                msg = userName + " updated successfully!";
            } else {

                Service service1 = new Service(userName, phoneNo, oxygen, type);
                service1.setStatus("Available");
                service1.setCompanyIdentifier(userAccount.getUsername());
                amb.getServiceDirectory().addNewService(service1);
            }

            resetForm();

            JOptionPane.showMessageDialog(this, msg,
                    "Success", JOptionPane.INFORMATION_MESSAGE);
            backAction();
            util.setDatabase(ecosystem);

        }
    }//GEN-LAST:event_btnAddActionPerformed

