     public void populateViewTable(){
        DefaultTableModel model = (DefaultTableModel) listTable.getModel();
        model.setRowCount(0);
            for(ConsultationForm st: consultDatalist){
                Object[] row = new Object[4];
                row[0] = st.getDiseaseName();
                row[1] = st.getRange();
                row[2] = st.getDays();
                row[3] = st.getMessage();
                model.addRow(row);
            }
     }
